package classes.object;

public class ObjectRefs {

    public static void main(String[] args) {
        Object io = 1;
        System.out.println(io);
        
        Object eo = 1 * 3 + 1.2;
        System.out.println(eo);
        
        Object bo = true || false && true;
        System.out.println(bo);
        
        Object so = 'a' + "b" + 2 * 3;
        System.out.println(so);

        
        
    }

}
